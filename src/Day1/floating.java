public class DivisionDemo {
    public static void main(String[] args) {
        int a = 17, b = 5;

        int intResult = a / b;
        double floatResult = (double) a / b;

        System.out.println("Integer Division (a/b) = " + intResult);
        System.out.println("Floating Point Division (a/b) = " + floatResult);
    }
}